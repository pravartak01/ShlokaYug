// Get challenges by category
export const getChallengesByCategory = (category: string): Challenge[] => {
  if (category === 'all') return CHALLENGES;
  return CHALLENGES.filter(c => c.category === category);
};

// Get challenges by difficulty
export const getChallengesByDifficulty = (difficulty: string): Challenge[] => {
  return CHALLENGES.filter(c => c.difficulty === difficulty);
};

// Get unlocked challenges for user level
export const getUnlockedChallenges = (userLevel: number): Challenge[] => {
  return CHALLENGES.filter(c => c.requiredLevel <= userLevel);
};

// Calculate XP for next level
export const calculateXpForLevel = (level: number): number => {
  return Math.floor(100 * Math.pow(1.5, level - 1));
};

// Get level from XP
export const getLevelFromXp = (xp: number): number => {
  let level = 1;
  let xpNeeded = 100;
  while (xp >= xpNeeded) {
    xp -= xpNeeded;
    level++;
    xpNeeded = calculateXpForLevel(level);
  }
  return level;
};
